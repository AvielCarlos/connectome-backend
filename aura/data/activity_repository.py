# ---------------------------------------------------------------------------
# Repository class
# ---------------------------------------------------------------------------

class ActivityRepository:
    """
    Provides access to the curated activity pool.
    Supports domain filtering, tag filtering, and exclusion lists.
    Can lazily expand to 500/domain by writing an LLM-generated cache.
    """

    def __init__(self):
        self._cache = None
        self._cache_loaded = False

    _cache: Optional[dict]
    _cache_loaded: bool

    def _load_cache(self):
        """Load additional LLM-generated activities from cache file, if present."""
        if self._cache_loaded:
            return
        self._cache_loaded = True
        if os.path.exists(CACHE_PATH):
            try:
                with open(CACHE_PATH, "r") as f:
                    data = json.load(f)
                self._cache = data
                logger.info(f"ActivityRepository: loaded cache with {sum(len(v) for v in data.values())} activities")
            except Exception as e:
                logger.warning(f"ActivityRepository: cache load failed: {e}")
                self._cache = None

    def get_activities_for_domain(
        self,
        domain: str,
        tags: list[str] | None = None,
        exclude_ids: list[str] | None = None,
        limit: int = 10,
    ) -> list[dict]:
        """
        Return random activities for a domain.
        
        Args:
            domain: "iVive" | "Eviva" | "Aventi"
            tags: optional list of tags to filter by (OR matching)
            exclude_ids: activity IDs to exclude (already shown to user)
            limit: how many to return
        """
        self._load_cache()

        base = list(ACTIVITIES_BY_DOMAIN.get(domain, []))

        # Merge cache if available
        if self._cache and domain in self._cache:
            cached_extras = self._cache[domain]
            base = base + [a for a in cached_extras if a.get("id") not in {x["id"] for x in base}]

        # Apply exclusion
        if exclude_ids:
            exclude_set = set(exclude_ids)
            base = [a for a in base if a.get("id") not in exclude_set]

        # Apply tag filter (OR match)
        if tags:
            tag_set = set(tags)
            filtered = [a for a in base if set(a.get("tags", [])) & tag_set]
            # Fall back to unfiltered if too few results
            if len(filtered) < limit:
                filtered = base
            base = filtered

        if not base:
            return []

        random.shuffle(base)
        return base[:limit]

    def get_random_activities(
        self,
        exclude_ids: list[str] | None = None,
        limit: int = 10,
    ) -> list[dict]:
        """Return random activities across all domains."""
        self._load_cache()
        pool = list(ALL_ACTIVITIES)
        if self._cache:
            for domain_activities in self._cache.values():
                pool.extend(domain_activities)
        if exclude_ids:
            exclude_set = set(exclude_ids)
            pool = [a for a in pool if a.get("id") not in exclude_set]
        random.shuffle(pool)
        return pool[:limit]

    async def maybe_expand_to_500(self, openai_client, domain: str):
        """
        If the domain has fewer than 500 activities in cache, use LLM to generate more.
        Saves to activities_cache.json. Called lazily at startup.
        """
        self._load_cache()
        current_cache = self._cache or {}
        domain_cache = current_cache.get(domain, [])
        base_count = len(ACTIVITIES_BY_DOMAIN.get(domain, []))
        total = base_count + len(domain_cache)

        if total >= 500:
            return  # Already have enough

        need = 500 - total
        logger.info(f"ActivityRepository: expanding {domain} by {need} activities via LLM")

        if not openai_client:
            return

        try:
            import json as _json
            prompt = f"""Generate {min(need, 100)} real, specific, actionable activities for the domain '{domain}'.
Domain descriptions:
- iVive: personal health, growth, identity, healing, inner work
- Eviva: community, contribution, impact, purpose, meaningful work  
- Aventi: experiences, adventure, play, culture, joy

Each activity should be genuinely useful, specific, and not generic.
Return a JSON array where each item has: id (slug), title, description (2 sentences), tags (array of 3-5 strings), difficulty (easy/medium/hard), time_required (string), cost (free/low/medium/high), domain (the domain string).

NO platitudes. NO "discover your potential" type language. Be specific and real.
Return ONLY a JSON array."""
            response = await openai_client.chat.completions.create(
                model="gpt-4o",
                messages=[{"role": "user", "content": prompt}],
                temperature=0.8,
                max_tokens=4000,
            )
            content = response.choices[0].message.content.strip()
            # Strip markdown if present
            if content.startswith("```"):
                content = content.split("```")[1]
                if content.startswith("json"):
                    content = content[4:]
            new_activities = _json.loads(content)
            if isinstance(new_activities, list):
                domain_cache.extend(new_activities)
                current_cache[domain] = domain_cache
                with open(CACHE_PATH, "w") as f:
                    _json.dump(current_cache, f, indent=2)
                self._cache = current_cache
                logger.info(f"ActivityRepository: saved {len(new_activities)} new activities for {domain}")
        except Exception as e:
            logger.warning(f"ActivityRepository: LLM expansion failed for {domain}: {e}")


# Singleton
_repository: ActivityRepository | None = None


def get_activity_repository() -> ActivityRepository:
    global _repository
    if _repository is None:
        _repository = ActivityRepository()
    return _repository
